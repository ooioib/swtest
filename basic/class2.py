# -----------------------------
# 부모 클래스: Animal
# 모든 동물의 공통 속성과 기능 정의
# -----------------------------
class Animal:
    # 생성자: 동물의 이름(n)을 받아 객체 속성으로 저장
    def __init__(self, n):
        self.name = n  # 객체별 이름 속성
    
    # 일반적인 동물의 소리 메서드
    # Animal 자체로 호출하면 "소리를 냅니다." 반환
    def speak(self):
        return "소리를 냅니다."


# -----------------------------
# 자식 클래스: Dog
# Animal 클래스를 상속받아 개의 특징 추가
# -----------------------------
class Dog(Animal):
    # speak() 메서드 오버라이딩
    # 부모 Animal의 speak() 대신 개 특화 메시지 반환
    def speak(self):
        return f"{self.name}가 멍멍 소리를 냅니다."


# -----------------------------
# 자식 클래스: Cat
# Animal 클래스를 상속받아 고양이 특징 추가
# -----------------------------
class Cat(Animal):
    # 생성자: 이름(n)과 색상(c) 받음
    def __init__(self, n, c):
        super().__init__(n)  # 부모 Animal의 생성자 호출하여 name 초기화
        self.color = c       # Cat만 가지는 추가 속성: color

    # speak() 메서드 오버라이딩
    def speak(self):
        return f"{self.color} 고양이 {self.name}가 야옹 소리를 냅니다."

# -----------------------------
# 객체 생성
# -----------------------------
dog = Dog("초코")   # Dog 객체, 이름 "초코"
cat = Cat("나비")   # Cat 객체, 이름 "나비"

# -----------------------------
# 메서드 호출
# -----------------------------
# Cat 클래스에서 오버라이딩한 speak() 실행
print(cat.speak())  # 출력: 나비가 야옹 소리를 냅니다.

# Dog 클래스에서 오버라이딩한 speak() 실행
print(dog.speak())  # 출력: 초코가 멍멍 소리를 냅니다.