# for 반복문 
# 1~10까지 합 구하기 ================

sum = 0

for i in range(1, 11):      # 1부터 10까지 숫자 생성
    sum = sum + i           # sum에 i 값을 계속 더해서 누적

print(sum)                  # 결과 : 55


