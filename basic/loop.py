# while문

i = 1                      # 시작값
while i < 11:              # i가 11보다 작은 동안 반복 (1~10)
    print(i)               # 현재 i 값 출력
    i = i + 2              # i 값을 2 증가 (1, 3,..., 9)


# while문

i = 0                      # 시작값
while i < 11:              # i가 11보다 작은 동안 반복 (1~10)
    i = i + 1              # i 값을 1 증가 (1,2,3,...,10)

    if i % 2 == 0:         # i를 2로 나눴을 때 나머지가 0이면 짝수
        continue           # 짝수면 아래 코드를 실행하지 않고 다음 반복으로 이동

    print(i)               # 짝수가 아니면(홀수면)만 출력


